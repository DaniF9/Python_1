def sumar(num1,num2):
    print("El resultado de la suma es : ",num1+num2)

def restar(num1,num2):
    print("El resultado de la restar es : ", num1-num2)

def multiplicar(num1,num2):
    print("El resultado de la multiplicar es : " , num1 * num2)

def potencia(base,exponente):
    print("El resultado de la potencia es : ", base ** exponente)

def dividir(num1,num2):
    print("El resultado de la division es : " ,  num1/num2)

def redondear(numero):
    print("El numero redondeado es : ", round(numero))
    
